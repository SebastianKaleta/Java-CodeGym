/* 
Iloczyn 10 liczb
*/

public class Solution {
    public static void main(String[] args) {
        //tutaj wpisz sw√≥j kod
        long iloczyn=1;
        for (int i = 1; i <= 10 ; i++) {
            iloczyn=iloczyn*i;
        }
        System.out.println(iloczyn);
    }
}
